  final String botanical;
  final String zona;
  final String light;
  final double precio;
  final int disponibilidad;
  final String imagen;

  Planta(
      {required this.nombre,
      required this.botanical,
      required this.zona,
      required this.light,
      required this.precio,
      required this.disponibilidad,
      required this.imagen});
}
